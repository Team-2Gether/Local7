CREATE TABLE RESTAURANTS (
    RESTAURANT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    ADDRESS VARCHAR2(200) NOT NULL,
    PHONE_NUMBER VARCHAR2(20),
    CATEGORY VARCHAR2(50),
    LATITUDE NUMBER(10, 7),
    LONGITUDE NUMBER(10, 7),
    OPEN_HOURS VARCHAR2(200),
    BREAK_TIME VARCHAR2(100),
    HOLIDAY VARCHAR2(100),
    PARKING_INFO VARCHAR2(200),
    AVERAGE_RATING NUMBER(2, 1) DEFAULT 0.0,
    REVIEW_COUNT NUMBER DEFAULT 0,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_RESTAURANTS_NAME ON RESTAURANTS (NAME);
CREATE INDEX IDX_RESTAURANTS_CATEGORY ON RESTAURANTS (CATEGORY);
CREATE INDEX IDX_RESTAURANTS_ADDRESS ON RESTAURANTS (ADDRESS);


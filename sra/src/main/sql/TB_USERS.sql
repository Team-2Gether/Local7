CREATE TABLE USERS (
    USER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    USERNAME VARCHAR2(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(255) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE,
    NICKNAME VARCHAR2(50) NOT NULL UNIQUE,
    PROFILE_IMAGE_URL VARCHAR2(500),
    BIO VARCHAR2(200),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP
);

-- 1. 회원 정보 테이블 (TB_USERS) 주석 추가
COMMENT ON TABLE TB_USERS IS '사용자 계정 정보를 저장하는 테이블';
COMMENT ON COLUMN TB_USERS.USER_ID IS '사용자 고유 ID (기본 키)';
COMMENT ON COLUMN TB_USERS.USERNAME IS '사용자 로그인 계정명 (고유)';
COMMENT ON COLUMN TB_USERS.PASSWORD IS '사용자 비밀번호 (해시값)';
COMMENT ON COLUMN TB_USERS.EMAIL IS '사용자 이메일 주소 (고유)';
COMMENT ON COLUMN TB_USERS.NICKNAME IS '사용자 닉네임 (고유)';
COMMENT ON COLUMN TB_USERS.PROFILE_IMAGE_URL IS '프로필 이미지 URL';
COMMENT ON COLUMN TB_USERS.BIO IS '사용자 자기소개';
COMMENT ON COLUMN TB_USERS.CREATED_AT IS '계정 생성일시';
COMMENT ON COLUMN TB_USERS.UPDATED_AT IS '계정 정보 수정일시';